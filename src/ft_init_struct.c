#include "../include/wolf3d.h"

void		ft_init_struct(t_a *a, int option)
{
	if (option == 0)
	{
		a->b.count = 0;
		a->b.countsp = 0;
		a->b.tmp = 0;
		a->b.tmpsp = 0;
		a->b.i = -1;
		a->b.countnl = 0;
	}
}
